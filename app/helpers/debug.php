    $mtime = $mtime[1] + $mtime[0];
    $starttime = $mtime;
}

register_shutdown_function(function() {
    global $starttime;
    $mtime = microtime();
    $mtime = explode(" ",$mtime);
    $mtime = $mtime[1] + $mtime[0];
    $endtime = $mtime;
    $totaltime = ($endtime - $starttime);
    pr("This page was created in ".$totaltime." seconds");
    pr(get_required_files());
});